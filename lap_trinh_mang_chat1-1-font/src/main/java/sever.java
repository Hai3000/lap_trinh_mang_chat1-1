    class clientHandler implements Runnable {

 private Socket socket;
    private BufferedReader reader;
    private PrintWriter writer;
     private Object hienthinlog;

     
     private void ghiTinNhanVaoFile(String msg) {
    try (FileWriter fw = new FileWriter("server_log.txt", true);
         BufferedWriter bw = new BufferedWriter(fw);
         PrintWriter out = new PrintWriter(bw)) {
        out.println(msg);
    } catch (IOException e) {
        hienthilog.append("‚ö† L·ªói khi ghi tin nh·∫Øn v√†o file: " + e.getMessage() + "\n");
    }
}

     
    public clientHandler(Socket socket) throws IOException {
        this.socket = socket;
        reader = new BufferedReader(new InputStreamReader(socket.getInputStream()));
        writer = new PrintWriter(socket.getOutputStream(), true);
        sever.clientWriters.add(writer);
    }

    @Override
public void run() {
    try {
        String msg;
        while ((msg = reader.readLine()) != null) {
            
              ghiTinNhanVaoFile(msg); // ‚úÖ Ghi tin nh·∫Øn chat v√†o file
            // üîπ G·ª≠i tin cho t·∫•t c·∫£ client KH√ÅC (b·ªè qua ng∆∞·ªùi g·ª≠i)
            synchronized (sever.clientWriters) {
                for (PrintWriter w : sever.clientWriters) {
                    if (w != writer) {  // ‚ùó B·ªè qua ch√≠nh ng∆∞·ªùi g·ª≠i
                        w.println(msg);
                    }
                }
            }

            // üîπ Ghi log l√™n giao di·ªán server
            String finalMsg = msg;
            javax.swing.SwingUtilities.invokeLater(() -> {
                hienthilog.append("\n" + finalMsg + " -> ");
            });
        }
    } catch (IOException e) {
        javax.swing.SwingUtilities.invokeLater(() -> {
            hienthilog.append("‚ö† Client ng·∫Øt k·∫øt n·ªëi.\n");
        });
    } finally {
        // üîπ X√≥a writer kh·ªèi danh s√°ch khi client ng·∫Øt k·∫øt n·ªëi
        synchronized (sever.clientWriters) {
            sever.clientWriters.remove(writer);
        }

        try {
            socket.close();
        } catch (IOException ignored) {}
    }

}
    }
